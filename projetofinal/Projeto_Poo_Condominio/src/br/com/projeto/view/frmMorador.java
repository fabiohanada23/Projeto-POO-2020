    public void listar() {
        MoradorDAO dao = new MoradorDAO();
        List<Morador> lista = dao.listarMorador();

        DefaultTableModel dados = (DefaultTableModel) tabelaMorador.getModel();
        dados.setNumRows(0);
        for (Morador c : lista) {
            dados.addRow(new Object[]{
                c.getId_morador(),
                c.getNome(),
                c.getCpf(),
                c.getEmail(),
                c.getCelular(),});
        }
    }

