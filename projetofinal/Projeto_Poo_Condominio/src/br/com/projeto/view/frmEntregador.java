    private void txtPesquisaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPesquisaKeyPressed

        // botao pesquisar
        String nome = "%" + txtPesquisa.getText() + "%";
        EntregadorDAO dao = new EntregadorDAO();
        List<Entregador> lista = dao.buscaClientePorEntregador(nome);
        DefaultTableModel dados = (DefaultTableModel) tabelaEntregador.getModel();
        dados.setNumRows(0);

        for (Entregador c : lista) {
            dados.addRow(new Object[]{
                c.getId_entregador(),
                c.getNome(),
                c.getCpf(),
                c.getAp_visita(),
                c.getTipo_entrega()
            });
        }

    }//GEN-LAST:event_txtPesquisaKeyPressed

