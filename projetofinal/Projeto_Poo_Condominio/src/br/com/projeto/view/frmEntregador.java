    private void btnbuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscaActionPerformed
        // botao busca por nome
        String nome = txtNome.getText();
        Entregador obj = new Entregador();
        EntregadorDAO dao = new EntregadorDAO();

        obj = dao.consultaPorNome(nome);
        
        if(obj.getNome() != null){
        //exibir
        txtCodigo.setText(String.valueOf(obj.getId_entregador()));
        txtNome.setText(obj.getNome());
        txtCpf.setText(obj.getCpf());
        txtApvisita.setText(obj.getAp_visita());
        txtTipoentrega.setText(obj.getTipo_entrega());
        }else{
            JOptionPane.showMessageDialog(null, "Entregador nao encontrado");
        }

    }//GEN-LAST:event_btnbuscaActionPerformed

